-- ═══════════════════════════════════════════════════════════════
-- Fix User Role - تنظیم role کاربر
-- ═══════════════════════════════════════════════════════════════

-- ✅ گام 1: بررسی role فعلی
-- ═══════════════════════════════════════════════════════════════
SELECT 
  id,
  email,
  role,
  created_at
FROM profiles
ORDER BY created_at DESC
LIMIT 5;

-- اگر role = NULL یا چیز دیگری بود، به گام 2 برو

-- ═══════════════════════════════════════════════════════════════
-- ✅ گام 2: تنظیم role به student
-- ═══════════════════════════════════════════════════════════════
-- ⚠️ جایگزین YOUR_EMAIL کنید

UPDATE profiles 
SET role = 'student'
WHERE email = 'YOUR_EMAIL@example.com';

-- نتیجه مورد انتظار: "UPDATE 1"

-- ═══════════════════════════════════════════════════════════════
-- ✅ گام 3: بررسی تغییرات
-- ═══════════════════════════════════════════════════════════════
SELECT 
  id,
  email,
  role
FROM profiles
WHERE email = 'YOUR_EMAIL@example.com';

-- نتیجه مورد انتظار: role = 'student'

